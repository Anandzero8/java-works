public class Swapping {
    public static void main(String[] args) {
        int x = 11;
        int y = 3;
        int temp = x;
        x = y;
        y = temp;
        System.out.println(x + " " + y);


        int A = 2;
        int B = 5;
        int C = A;
        A = B;
        B = C;
        System.out.println(A + " " + B);
    }
}
